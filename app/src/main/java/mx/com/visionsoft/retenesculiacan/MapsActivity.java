    private void addMarkersToMap(final GoogleMap map) {
        mChildEventListener = mProfileRef2.addChildEventListener(new ChildEventListener() {
            @Override
            public void onChildAdded(DataSnapshot dataSnapshot, String s) {
                //Toast.makeText(mContext, "Se agrego un reten", Toast.LENGTH_SHORT).show();
                FireBaseMaker marker = dataSnapshot.getValue(FireBaseMaker.class);
                String fecha = marker.getFecha();
                String desc = marker.getDescripcion();
                Double latitude = marker.getLatitude();
                Double longitude = marker.getLongitude();
                LatLng location = new LatLng(latitude,longitude);
                map.addMarker(new MarkerOptions().position(location).title(desc).snippet(fecha).icon(BitmapDescriptorFactory.fromResource(R.drawable.patrulla3)));
            }

            @Override
            public void onChildChanged(DataSnapshot dataSnapshot, String s) {

            }

            @Override
            public void onChildRemoved(DataSnapshot dataSnapshot) {
                Toast.makeText(mContext, "Se elimino un reten", Toast.LENGTH_SHORT).show();
                // Removes all markers, overlays, and polylines from the map.
                map.clear();
                FireBaseMaker marker = dataSnapshot.getValue(FireBaseMaker.class);
                String fecha = marker.getFecha();
                String desc = marker.getDescripcion();
                Double latitude = marker.getLatitude();
                Double longitude = marker.getLongitude();
                LatLng location = new LatLng(latitude,longitude);
                map.addMarker(new MarkerOptions().position(location).title(desc).snippet(fecha).icon(BitmapDescriptorFactory.fromResource(R.drawable.patrulla3)));
            }

            @Override
            public void onChildMoved(DataSnapshot dataSnapshot, String s) {

            }

            @Override
            public void onCancelled(DatabaseError databaseError) {

            }
        });
    }

